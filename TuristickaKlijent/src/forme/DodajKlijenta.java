    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String ime = txtImeKlijenta.getText();
        String prezime = txtPrezimeKlijenta.getText();
        String brojTelefona = txtBrojTelefona.getText();
        String adresa = txtAdresa.getText();
        
        if (ime.isEmpty() || prezime.isEmpty() || brojTelefona.isEmpty() || adresa.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Sva polja moraju biti popunjena");
            return;
        }
        
        Mesto mesto = (Mesto) cmbMesto.getSelectedItem();
        
        Klijent klijent = new Klijent(-1, ime, prezime, brojTelefona, adresa, mesto);
        
        KlijentskiZahtev kz = new KlijentskiZahtev(Operacije.UNOS_KLIJENTA, klijent);
        Komunikacija.getInstanca().posaljiZahtev(kz);
        
        ServerskiOdgovor so = Komunikacija.getInstanca().primiOdgovor();
        
        if (so.isUspesno()) {
            JOptionPane.showMessageDialog(this, so.getPoruka());
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(this, so.getPoruka());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

