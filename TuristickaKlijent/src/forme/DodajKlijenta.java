    private void popuniMesta() {
        KlijentskiZahtev kz = new KlijentskiZahtev(Operacije.VRATI_MESTA, new Mesto());
        Komunikacija.getInstanca().posaljiZahtev(kz);
        
        ServerskiOdgovor so = Komunikacija.getInstanca().primiOdgovor();
        List<OpstiDomenskiObjekat> lista = (List<OpstiDomenskiObjekat>) so.getOdgovor();
        
        cmbMesto.removeAllItems();
        
        for (OpstiDomenskiObjekat opstiDomenskiObjekat : lista) {
            cmbMesto.addItem(opstiDomenskiObjekat);
        }
    }
