    private void popuniKomboHotela() {
        KlijentskiZahtev kz = new KlijentskiZahtev(Operacije.VRATI_HOTELE, new Hotel());
        Komunikacija.getInstanca().posaljiZahtev(kz);
        ServerskiOdgovor so = Komunikacija.getInstanca().primiOdgovor();
        
        List<OpstiDomenskiObjekat> lista = (List<OpstiDomenskiObjekat>) so.getOdgovor();
        
        cmbHotel.removeAllItems();
        
        for (OpstiDomenskiObjekat opstiDomenskiObjekat : lista) {
            cmbHotel.addItem(opstiDomenskiObjekat);
        }
    }

    private void popuniKomboTransporta() {
        KlijentskiZahtev kz = new KlijentskiZahtev(Operacije.VRATI_TRANSPORTE, new Transport());
        Komunikacija.getInstanca().posaljiZahtev(kz);
        ServerskiOdgovor so = Komunikacija.getInstanca().primiOdgovor();
        
        List<OpstiDomenskiObjekat> lista = (List<OpstiDomenskiObjekat>) so.getOdgovor();
        
        cmbTransport.removeAllItems();
        
        for (OpstiDomenskiObjekat opstiDomenskiObjekat : lista) {
            cmbTransport.addItem(opstiDomenskiObjekat);
        }
    }
