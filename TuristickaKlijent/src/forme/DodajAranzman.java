    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            Date datumOd = sdf.parse(txtDatumOd.getText());
            Date datumDo = sdf.parse(txtDatumDo.getText());
            Hotel hotel = (Hotel) cmbHotel.getSelectedItem();
            
            HotelAranzman ha = new HotelAranzman(null, hotel, datumOd, datumDo);
            ModelTabeleHotelAranzman mta = (ModelTabeleHotelAranzman) tblHotelAranzman.getModel();
            mta.dodaj(ha);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Datum mora biti u formatu dd.MM.yyyy");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

