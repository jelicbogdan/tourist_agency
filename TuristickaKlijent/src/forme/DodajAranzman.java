    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String destinacija = txtDestinacija.getText();
        
        if(destinacija.isEmpty()){
            JOptionPane.showMessageDialog(this, "Destinacija mora biti popunjena");
            return;
        }
        
       ModelTabeleHotelAranzman mha = (ModelTabeleHotelAranzman) tblHotelAranzman.getModel();
       ModelTabeleTransportAranzman mta = (ModelTabeleTransportAranzman) tblTransportAranzman.getModel();
       
       List<OpstiDomenskiObjekat> listaHa = mha.getLista();
       List<OpstiDomenskiObjekat> listaTa = mta.getLista();
       
       
       Prodavac prodavac = ((GlavnaForma)getParent()).getUlogovaniProdavac();
       
       Aranzman a = new Aranzman(-1, destinacija, prodavac, listaHa, listaTa);
       
       KlijentskiZahtev kz = new KlijentskiZahtev(Operacije.SACUVAJ_ARANZMAN, a);
        Komunikacija.getInstanca().posaljiZahtev(kz);
        
        ServerskiOdgovor so = Komunikacija.getInstanca().primiOdgovor();
        
        if (so.isUspesno()) {
            JOptionPane.showMessageDialog(this, so.getPoruka());
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(this, so.getPoruka());
        }
       
    }//GEN-LAST:event_jButton5ActionPerformed

