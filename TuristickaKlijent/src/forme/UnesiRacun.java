    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Aranzman a = (Aranzman) cmbAranzmani.getSelectedItem();
        int popust = Integer.parseInt(txtPopust.getText());
        double iznos = Double.parseDouble(txtIznosStavke.getText());

        StavkaRacuna sr = new StavkaRacuna(-1, null, a, popust, iznos);

        ModelTabeleStavke mts = (ModelTabeleStavke) tblStavke.getModel();
        List<OpstiDomenskiObjekat> lista = mts.getLista();

        for (OpstiDomenskiObjekat opstiDomenskiObjekat : lista) {
            StavkaRacuna srTabela = (StavkaRacuna) opstiDomenskiObjekat;
            if (srTabela.getAranzman().getSifraAranzmana() == a.getSifraAranzmana()) {
                JOptionPane.showMessageDialog(this, "Stavka za ovaj aranzman vec postoji za ovaj racun");
                return;
            }
        }

        mts.dodaj(sr);
        izracunajUkupno();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int red = tblStavke.getSelectedRow();
        if (-1 != red) {
            ModelTabeleStavke mts = (ModelTabeleStavke) tblStavke.getModel();
            mts.obrisi(red);
            izracunajUkupno();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        ModelTabeleStavke mts = (ModelTabeleStavke) tblStavke.getModel();
        Klijent klijent = (Klijent) cmbKlijenti.getSelectedItem();
        double ukupno = Double.parseDouble(txtUkupnaVrednost.getText());
        List<OpstiDomenskiObjekat> lista = mts.getLista();
        Prodavac prodavac = ((GlavnaForma) getParent()).getUlogovaniProdavac();

        Racun racun = new Racun(-1, klijent, prodavac, new Date(), ukupno, lista);

        KlijentskiZahtev kz = new KlijentskiZahtev(Operacije.SACUVAJ_RACUN, racun);
        Komunikacija.getInstanca().posaljiZahtev(kz);

        ServerskiOdgovor so = Komunikacija.getInstanca().primiOdgovor();
        
        JOptionPane.showMessageDialog(this, so.getPoruka());

    }//GEN-LAST:event_jButton3ActionPerformed

