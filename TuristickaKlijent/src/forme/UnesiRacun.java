    private void popuniKlijente() {
        Klijent k = new Klijent();
        k.setImeKlijenta("");

        KlijentskiZahtev kz = new KlijentskiZahtev(Operacije.PRETRAGA_KLIJENTA, k);
        Komunikacija.getInstanca().posaljiZahtev(kz);

        ServerskiOdgovor so = Komunikacija.getInstanca().primiOdgovor();

        List<OpstiDomenskiObjekat> lista = (List<OpstiDomenskiObjekat>) so.getOdgovor();

        cmbKlijenti.removeAllItems();

        for (OpstiDomenskiObjekat opstiDomenskiObjekat : lista) {
            cmbKlijenti.addItem(opstiDomenskiObjekat);
        }
    }

    private void popuniAranzmanje() {
        Aranzman a = new Aranzman();
        a.setDestinacija("");

        KlijentskiZahtev kz = new KlijentskiZahtev(Operacije.PRETRAZI_ARANZMAN, a);
        Komunikacija.getInstanca().posaljiZahtev(kz);

        ServerskiOdgovor so = Komunikacija.getInstanca().primiOdgovor();

        List<OpstiDomenskiObjekat> lista = (List<OpstiDomenskiObjekat>) so.getOdgovor();

        cmbAranzmani.removeAllItems();

        for (OpstiDomenskiObjekat opstiDomenskiObjekat : lista) {
            cmbAranzmani.addItem(opstiDomenskiObjekat);
        }
    }

    private void izracunajUkupno() {
        ModelTabeleStavke mts = (ModelTabeleStavke) tblStavke.getModel();
        double ukupno = 0.00;

        List<OpstiDomenskiObjekat> lista = mts.getLista();

        for (OpstiDomenskiObjekat opstiDomenskiObjekat : lista) {
            StavkaRacuna srTabela = (StavkaRacuna) opstiDomenskiObjekat;
            ukupno += srTabela.izracuanIznosSaPopustom();
        }

        txtUkupnaVrednost.setText("" + ukupno);
    }
